package com.vsii.spring.bookstore.model;


public class Book {
	String title;
	Category category;
	
	public String getTitle() {
	
		return title;
	}
	
	public void setTitle(String title) {
	
		this.title = title;
	}
	
	public Category getCategory() {
	
		return category;
	}
	
	public void setCategory(Category category) {
	
		this.category = category;
	}
	
	
}
